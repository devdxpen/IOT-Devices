export async function addDevice(
  device: Partial<DeviceSummary>,
): Promise<DeviceSummary> {
  // Simulate network delay
  await new Promise((resolve) => setTimeout(resolve, 600));

  const newDevice: DeviceSummary = {
    id: `device-new-${Date.now()}`,
    name: device.name || "Unknown Device",
    type: device.type || "General Type",
    serialNumber: device.serialNumber || "N/A",
    location: device.location || "unknown",
    category: device.category || "General",
    subCategory: device.subCategory || "General",
    users: [],
    userCount: 0,
    manufacturer: device.manufacturer || "Unknown Brand",
    model: device.model || "Unknown Model",
    firmwareVersion: "1.0.0",
    macAddress: device.macAddress || "00:00:00:00:00:00",
    icon: "/sensor-icon.png",
    // New fields
    company: device.company || "Blue Star",
    assignedUser: device.assignedUser || {
      name: "Anna Adame",
      avatarUrl: "/avatars/avatar-1.png",
    },
    lastDataTimestamp: device.lastDataTimestamp || "Just Now",
    alarms: device.alarms || 0,
    data: device.data || { t1: 0, t2: 0, t3: 0 },
    position: mockDeviceSummaries.length + 1,
    isOnline: device.isOnline ?? true,
    ownership: device.ownership || "own",
    tags: device.tags || ["New"],
    colorFlag: device.colorFlag || "none",
    // Merge any other passed values
    ...device,
  };

  // Prepend to array
  mockDeviceSummaries = [newDevice, ...mockDeviceSummaries];
  // Re-index positions since we prepended
  mockDeviceSummaries.forEach((d, idx) => {
    d.position = idx + 1;
  });
  return newDevice;
}

export async function updateDevicePriority(
  deviceId: string,
  targetPosition: number,
  newFlag: "red" | "yellow" | "green" | "none",
): Promise<void> {
  // Simulate network delay
  await new Promise((resolve) => setTimeout(resolve, 300));

  const currentIndex = mockDeviceSummaries.findIndex((d) => d.id === deviceId);
  if (currentIndex === -1) return;

  const device = mockDeviceSummaries[currentIndex];

  // Update the flag explicitly
  device.colorFlag = newFlag;

  // Remove item from current position
  mockDeviceSummaries.splice(currentIndex, 1);

  // Guard target index
  const safeTargetIndex = Math.max(
    0,
    Math.min(targetPosition - 1, mockDeviceSummaries.length),
  );

  // Insert item at new position
  mockDeviceSummaries.splice(safeTargetIndex, 0, device);

  // Recalculate explicit positions for all items
  mockDeviceSummaries.forEach((d, idx) => {
    d.position = idx + 1;
  });
}
